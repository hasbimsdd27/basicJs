const a = 10;
const b = 10;
const c = 13;

// lebih dari atau >
console.log(a > b);

// kurang dari atau <
console.log(a < c);

// sama dengan double atau = =
// membandingkan nilai tanpa memperdulikan tipe data nya.
console.log(a == '10');

// sama dengan triple atau = = =
// membandingkan nilai dan tipe data
console.log(a === '10');

console.log(a != '5');
console.log(a !== 10);
console.log(a >= 2);
console.log(a <= 100);